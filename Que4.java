class QUe4 {
    public static void main(String[] args) {
        // Declare and initialize three int values
        int a = 10;
        int b = 20;
        int c = 30;

        // Calculate the average by typecasting to double
        double avg = (double)(a + b + c) / 3;

        // Display the result
        System.out.println("The average is: " + avg);
    }
}
