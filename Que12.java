class Que12{
    public static void main(String [] args){
	     // Declare two integers
        int num1 = 10, num2 = 5;

        

        
        num1 += num2; 
        System.out.println( num1);

        num1 -= num2; 
        System.out.println( num1);
                                       
        num1 *= num2; 
        System.out.println( num1);

        num1 /= num2; 
        System.out.println( num1);

        num1 %= num2; 
        System.out.println( num1);
    	
	}
}